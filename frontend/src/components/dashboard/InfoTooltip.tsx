export function MetricInfo({ metricKey }: { metricKey: keyof typeof METRIC_DEFINITIONS }) {
  const metric = METRIC_DEFINITIONS[metricKey]
  if (!metric) return null

  return <InfoTooltip label={metric.name} tooltip={metric.definition} learnMore={metric.learnMore} />
}

