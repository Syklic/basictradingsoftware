import { Info, ChevronDown } from 'lucide-react'
import { useState } from 'react'

interface InfoTooltipProps {
  label: string
  tooltip: string
  learnMore?: string
}

export function InfoTooltip({ label, tooltip, learnMore }: InfoTooltipProps) {
  const [showTooltip, setShowTooltip] = useState(false)

  return (
    <div className="relative inline-flex items-center gap-1">
      <span className="font-medium text-sm">{label}</span>
      <div
        className="relative"
        onMouseEnter={() => setShowTooltip(true)}
        onMouseLeave={() => setShowTooltip(false)}
      >
        <button
          className="p-0.5 hover:bg-muted rounded transition-colors"
          title={tooltip}
          aria-label={`Info: ${tooltip}`}
        >
          <Info className="h-4 w-4 text-muted-foreground hover:text-foreground transition-colors" />
        </button>

        {/* Tooltip */}
        {showTooltip && (
          <div className="absolute left-0 bottom-full mb-2 w-48 bg-card border border-border rounded-lg p-2 text-xs text-muted-foreground shadow-lg z-40 before:absolute before:left-2 before:top-full before:w-2 before:h-2 before:bg-card before:border-r before:border-b before:border-border before:rotate-45">
            {tooltip}
            {learnMore && (
              <div className="mt-2 pt-2 border-t border-border text-xs text-accent">
                {learnMore}
              </div>
            )}
          </div>
        )}
      </div>
    </div>
  )
}

interface ExpandableInfoProps {
  title: string
  description: string
  children?: React.ReactNode
}

export function ExpandableInfo({ title, description, children }: ExpandableInfoProps) {
  const [isExpanded, setIsExpanded] = useState(false)

  return (
    <div className="rounded-lg border border-border overflow-hidden">
      <button
        onClick={() => setIsExpanded(!isExpanded)}
        className="w-full flex items-center justify-between p-3 hover:bg-muted/50 transition-colors"
      >
        <div className="flex items-center gap-2">
          <Info className="h-4 w-4 text-accent flex-shrink-0" />
          <span className="font-medium text-sm">{title}</span>
        </div>
        <ChevronDown
          className={`h-4 w-4 text-muted-foreground transition-transform ${
            isExpanded ? 'rotate-180' : ''
          }`}
        />
      </button>

      {isExpanded && (
        <div className="border-t border-border p-3 bg-muted/30 text-sm text-muted-foreground space-y-2">
          <p>{description}</p>
          {children}
        </div>
      )}
    </div>
  )
}

